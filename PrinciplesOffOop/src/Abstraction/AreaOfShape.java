package Abstraction;

public abstract class AreaOfShape {

	public abstract void areaOfCircle(double r);
	
	public abstract void areaOfRectangle(double l, double b); 
	
	public abstract void areaOfSquare(double l);
	
	public abstract void areaOfParallelogram(double l, double h);
	
	public abstract void areaOfRhombus(double b, double h); // diagonal multiplication 
	
}
